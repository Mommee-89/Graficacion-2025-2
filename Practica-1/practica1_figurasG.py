import turtle

pantalla = turtle.Screen()
pantalla.bgcolor("white")
t = turtle.Turtle()
t.pensize(2)
t.speed(3)

t.penup()
t.goto(-200, 100)
t.pendown()
t.color("blue")
t.forward(100)
t.right(90)
t.forward(100)
t.right(90)
t.forward(100)
t.right(90)
t.forward(100)
t.right(90)

t.penup()
t.goto(0, 100)
t.pendown()
t.color("green")
t.forward(100)
t.right(120)
t.forward(100)
t.right(120)
t.forward(100)
t.right(120)

t.penup()
t.goto(200, 50)
t.pendown()
t.color("red")
t.circle(50)

t.penup()
t.goto(-100, -100)
t.setheading(0)  
t.pendown()
t.color("black")
t.forward(200)

turtle.done()
